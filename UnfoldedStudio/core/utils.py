def extract_color(color: str):
    """ Extract rgb, hex and aplha values from color string """
    _color = list(map(int, color.split(",")))
    rgb_value = _color[:-1]
    hex_value = '#{:02x}{:02x}{:02x}'.format(*rgb_value)
    alpha = _color[-1] / 255.0
    return rgb_value, hex_value, alpha
